package basics;

public class datatype {

	public static void main(String[] args) {
		System.out.println("datatype ");
		System.out.println("max size of byte :"+Byte.MAX_VALUE);
		System.out.println("max size of int :"+Integer.MAX_VALUE);
		System.out.println("max size of Long :"+Long.MAX_VALUE);
		System.out.println("max size of char :"+Character.MAX_VALUE);
		
		
		long a=9090909090l;
		System.out.println("longe value is : "+a);
		double b=1010101.23;
		System.out.println("value of double is : "+b);
		float f=123.34f;
		System.out.println("value of float is : "+f);
		boolean o = true;
		System.out.println("value of boolean is : "+o );
		char ch='p';
		System.out.println("value of character is : "+ch);
		String st="%^abhj87";
		System.out.println("value of string is " +st);
		
	}

}
